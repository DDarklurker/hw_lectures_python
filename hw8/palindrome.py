"""
В бескінечному циклі користувач вводить строки. Відповідь чи є строка паліндромом - дорівнює сама собі задом на перед.
Переривається при вводі пустої строки
"""
# ок. можна було трошки легше string == string[::-1]
while True:
    your_line = (input('Щось введіть: '))
    if your_line == '':
        break
    if your_line == ''.join(reversed(your_line)):
        print("строка є паліндромом")
    else:
        print("строка не є паліндромом")
